/* Power Query

El editor de Power Query representa la interfaz de usuario de Power Query, donde puede agregar o modificar consultas, 
administrar consultas agrupando o agregando descripciones a los pasos de consulta o visualizar las consultas y su estructura 
con diferentes vistas. La interfaz de usuario de Power Query tiene cinco componentes distintos.

consulta: las conexiones a ditintas tablas o archivos que tenemos 

1. La cinta de opciones
La cinta de opciones es el componente donde encontrará la mayoría de las transformaciones y acciones que puede realizar en el editor de Power Query. 
Tiene múltiples pestañas, cuyos valores dependen de la integración del producto. Cada una de las pestañas proporciona botones y opciones específicos, 
algunos de los cuales pueden ser redundantes en toda la experiencia de Power Query. Estos botones y opciones le proporcionan un fácil acceso a las transformaciones y acciones que pueda necesitar.


- elaboracion de perfiles de datos: calidad de las columnas, ademas nos ayuda con la distribucion de la misma (si tiene datos vacios o datos nulos)

- editor avanzado: Si desea observar el código que el editor de Power Query está creando con cada paso, o desea crear su propio código de conformación, puede usar el editor avanzado.4
                   Para abrir el editor avanzado, seleccione la pestaña Ver en la cinta de opciones y, a continuación, seleccione Editor avanzado. Aparece una ventana que muestra el
                   código de consulta existente. (permite manejar codigo)
                   
- pasos aplicados: Cada transformación que se aplica a la consulta se guarda como un paso en la sección Pasos aplicados del panel de configuración de la consulta.
                   Si alguna vez necesita comprobar cómo se transforma la consulta de paso a paso, puede seleccionar un paso y obtener una vista previa
                   de cómo se resuelve la consulta en ese momento específico.

