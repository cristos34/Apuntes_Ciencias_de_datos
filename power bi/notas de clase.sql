evolucon del proceso:

1- que paso? -> reporte ad-hoc "evento puntual"
2- que esta pasando? -> reporte automatizado
3- que esta pasando y porque? -> analisis descriptivo
4- que va pasar? -> analisis predictivo
5- como podemos lograrlo? -> analisis prescriptivo


analisis descriptivo: se utiliza agregacion y mineria de datos para proporcionar una vision del pasado y responder ¿que ha sucedido?

analisis predictivo: se utiliza modelos estadisticos y tecnicas de pronosticos para comprender el futuro y responder ¿que podria pasar?

analisis prescriptivo: se utilizan algoritmos de optimizacion y simulacion para asesorar sobre posibles resultads y responder  ¿que debemos hacer?


